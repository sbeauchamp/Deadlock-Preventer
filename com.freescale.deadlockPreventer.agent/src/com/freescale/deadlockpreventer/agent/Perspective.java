package com.freescale.deadlockpreventer.agent;

import org.eclipse.ui.IPageLayout;
import org.eclipse.ui.IPerspectiveFactory;

public class Perspective implements IPerspectiveFactory {

	/**
	 * The ID of the perspective as specified in the extension.
	 */
	public static final String ID = "com.freescale.deadlockpreventer.agent.perspective";

	public void createInitialLayout(IPageLayout layout) {
		String editorArea = layout.getEditorArea();
		layout.setEditorAreaVisible(false);
		layout.setFixed(true);
		
		layout.addStandaloneView(LauncherView.ID,  false, IPageLayout.LEFT, 0.25f, editorArea);
		layout.getViewLayout(LauncherView.ID).setCloseable(false);
	}
}
